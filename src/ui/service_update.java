    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {       
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","");
             PreparedStatement pt = con.prepareStatement("update service set customer = ?,product = ?,brand = ?,model = ?,complaint = ?,estimatecost = ?,expecteddelivery = ?,status = ?,remarks = ? ");
             pt.setString(1,cname.getText());
             pt.setString(2,product.getText());
             pt.setString(3,brand.getText());
             pt.setString(4,model.getText());
             pt.setString(5,complaint.getText());
             pt.setInt(6,Integer.parseInt(cost.getText()));
             pt.setString(7,delivery.getText());
             pt.setString(8, (String) status.getSelectedItem());
             pt.setString(9,remarks.getText());
             pt.executeUpdate();
             if (status.getSelectedItem().equals("Returned")) {
                Map<String,Object> params = new HashMap<>();
            params.put("sid",sid.getSelectedItem());
            params.put("dos",date.getText());
            params.put("name",cname.getText());
            params.put("product",product.getText());
            params.put("brand",brand.getText());
            params.put("model",model.getText());
            params.put("complaint",complaint.getText());
            params.put("cost", cost.getText());
           // params.put("model",model.getText());
            params.put("status",status.getSelectedItem());
            JasperDesign jd = JRXmlLoader.load("report/servicebill.jrxml");
            JasperReport jp = JasperCompileManager.compileReport(jd);
          //JRDataSource datasource = new JRTableModelDataSource(jTable1.getModel());
            JasperPrint print = JasperFillManager.fillReport(jp,params,new JREmptyDataSource());
            JasperViewer.viewReport(print,true);
            }
             JOptionPane.showMessageDialog(null,"Updated");
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

