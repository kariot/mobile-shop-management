    private void sidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_sidItemStateChanged
       int s;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            System.out.println("Driver loaded successfully");  
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","");
            Statement ps = con.createStatement();
            ResultSet rs = ps.executeQuery("select * from service where id = "+sid.getSelectedItem());
            if (rs.next()) {
                if (rs.getString("status").equals("Recieved")) {
                    s = 0;
                } else if(rs.getString("status").equals("Serviced")) {
                    s=1;
                } else if (rs.getString("status").equals("Can't Service")) {
                    s=2;
                } else{
                    s=3;
                }
                date.setText(rs.getString("date"));
                cname.setText(rs.getString("customer"));
                product.setText(rs.getString("product"));
                brand.setText(rs.getString("brand"));
                model.setText(rs.getString("model"));
                complaint.setText(rs.getString("complaint"));
                cost.setText(rs.getString("estimatecost"));
                delivery.setText(rs.getString("expecteddelivery"));
                status.setSelectedIndex(s);
                remarks.setText(rs.getString("remarks"));   
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_sidItemStateChanged

